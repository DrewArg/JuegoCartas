package utilidades;

public class AsignadorCaracteristica {

    public static int asignarId(int contadorId, int mazoSeleccionado) {
        if (mazoSeleccionado == 0) {
            return contadorId;
        } else if (mazoSeleccionado == 1) {
            return contadorId;
        } else {
            return contadorId;
        }
    }

}